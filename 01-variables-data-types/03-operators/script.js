// Operators 

let number1 = 20;
let number2 = 10;

let sumOf = number1 + number2;
let diffOf = number1 - number2;
let productOf = number1 * number2;
let remainderOf = number1 % number2;
let quotientOf = number1 / number2;

console.log("The sum is: " + sumOf);
console.log("The difference is: " + diffOf);
console.log("The product is: " + productOf);
console.log("The remainder is: " + remainderOf);
console.log("The quotient is: " + quotientOf);

console.log(sumOf++);
console.log(diffOf++);
console.log(productOf++);
console.log(remainderOf++);
console.log(quotientOf++);

console.log(sumOf--);
console.log(diffOf--);
console.log(productOf--);
console.log(remainderOf--);
console.log(quotientOf--);
